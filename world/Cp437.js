const UNICODE_TO_CP437 = new Map(CP437_TO_UNICODE.map((char, code) => [char, code]));

export function decodeCp437(code, { nullAsSpace = true } = {}) {
  if (!Number.isInteger(code) || code < 0 || code > 255) return '?';
  if (code === 0 && nullAsSpace) return ' ';
  return CP437_TO_UNICODE[code] ?? '?';
}

export function encodeCp437(char) {
  if (!char || typeof char !== 'string') return 32;
  return UNICODE_TO_CP437.get(char[0]) ?? 63;
}

